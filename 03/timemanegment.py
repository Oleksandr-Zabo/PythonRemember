# 1. Создаем генератор фоновых дел
def background_tasks_generator():
    """
    Генератор, который хранит список мелких дел (2 степени важности).
    Он будет выдавать их по одному и ставить работу на паузу (yield),
    пока мы не попросим следующее дело.
    """
    tasks = ["Вынести мусор", "Починить обувь", "Полить цветы", "Разобрать почту"]

    for task in tasks:
        yield task  # Выдаем дело и "замораживаем" генератор


# Создаем объект генератора (список дел на сегодня)
# В этот момент ни одно дело еще не выполняется
bg_tasks = background_tasks_generator()

print("=== Начинаем цикл жизни! ===")

# 2. Основной цикл жизни (бесконечный цикл, пока не кончатся дела)
while True:
    print("\n" + "-" * 30)

    # Спрашиваем пользователя о текущих приоритетах
    answer = input("Есть ли сейчас важная срочная задача? (да/нет): ").strip().lower()

    if answer == 'да':
        # Если есть важная задача, мы делаем ее, не трогая генератор фоновых дел
        important_task = input("Что за важная задача?: ")
        print(f"🚀 Срочно выполняю: {important_task}")
        print("Фух, справился! Возвращаемся к рутине.")

    else:
        # Если срочных дел нет, "дергаем" генератор и просим выдать фоновую задачу
        try:
            # next() будит генератор, он выдает дело и снова засыпает
            current_bg_task = next(bg_tasks)
            print(f"🧹 Свободное время! Делаю фоновую задачу: {current_bg_task}")

        except StopIteration:
            # Когда генератор отдаст последнее дело, вызов next() выдаст ошибку StopIteration.
            # Мы её отлавливаем (обрабатываем) и красиво завершаем программу.
            print("\n🎉 Все фоновые дела закончились! Можно отдыхать!")
            break  # Выходим из бесконечного цикла жизни

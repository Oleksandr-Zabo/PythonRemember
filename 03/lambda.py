# Наша основная функция (функция высшего порядка)
# Она принимает часы работы (число) и само действие (лямбда-функцию)
def help_parents(hours, action_lambda):
    print(f"Я приехал в деревню к родителям! Готов работать {hours} часа(ов).")

    # Запускаем переданную лямбду, передавая ей часы
    result = action_lambda(hours)

    print(f"Что я делаю: {result}")
    print("Родители довольны, идем пить чай!\n")
    print("-" * 40)


# --- ПРАКТИКА: Передаем разные действия \"на лету\" ---

# 1. Операция: Роет огород
help_parents(3, lambda h: f"Усердно рою огород лопатой все {h} часа без остановки. 🚜")

# 2. Операция: Красит забор
# За один час я крашу 5 метров забора
help_parents(2, lambda h: f"Крашу забор. Успел покрасить {h * 5} метров! 🎨")

# 3. Операция: Носит воду
# За один час я приношу 10 ведер
help_parents(1, lambda h: f"Ношу воду из колодца. Принес целых {h * 10} ведер. 🪣")

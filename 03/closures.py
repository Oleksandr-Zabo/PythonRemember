def gather_backpack(matches, salt):
    """
    Внешняя функция. Это наш 'дом', где мы собираем рюкзак.
    Переменные matches и salt — это вещи, которые мы кладем внутрь.
    """
    print(f"🎒 Рюкзак собран! Спичек: {matches} шт., Соли: {salt} грамм.")

    # Внутренняя функция. Это наш 'поход'.
    # Она "запоминает" (замыкает в себе) переменные matches и salt.
    def go_camping(action, amount=1):
        # Ключевое слово nonlocal говорит питону:
        # "Бери эти переменные не из текущей функции, а из 'рюкзака' внешней!"
        nonlocal matches, salt

        if action == 'разжечь костер':
            if matches >= amount:
                matches -= amount
                print(f"🔥 Костер горит! Потрачено спичек: {amount}. Осталось: {matches}.")
            else:
                print("❌ Упс, спички закончились!")

        elif action == "посолить суп":
            if salt >= amount:
                salt -= amount
                print(f"🍲 Суп посолен! Потрачено соли: {amount}г. Осталось: {salt}г.")
            else:
                print("❌ Соли больше нет, суп будет пресным!")

    # ВАЖНО: Мы возвращаем САМУ функцию (поход), а не её результат (без скобок!)
    return go_camping


# === ПРАКТИКА ===

# 1. Мы "вышли из дома". Функция gather_backpack отработала и ЗАВЕРШИЛАСЬ.
# По правилам Питона, переменные matches и salt должны были навсегда стереться из памяти.
my_hike = gather_backpack(matches=10, salt=50)

print("\n--- Прошло два дня в лесу ---")

# 2. Но наша функция-поход (my_hike) "замкнула" их в себе (взяла рюкзак).
# Мы можем продолжать тратить наши спички и соль!
my_hike("разжечь костер", 2)  # Останется 8 спичек
my_hike("посолить суп", 10)  # Останется 40 грамм соли
my_hike("разжечь костер", 5)  # Останется 3 спички

print("\n--- Другой турист ---")
# 3. А если кто-то другой соберет свой рюкзак, у него будет СВОЯ независимая память!
friend_hike = gather_backpack(matches=2, salt=100)
friend_hike("разжечь костер", 3)  # У него не хватит спичек!
